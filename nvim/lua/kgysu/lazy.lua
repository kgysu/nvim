-- [[ Basic Autocommands ]]
--  See `:help lua-guide-autocommands`

-- Highlight when yanking (copying) text
vim.api.nvim_create_autocmd('TextYankPost', {
  desc = 'Highlight when yanking (copying) text',
  group = vim.api.nvim_create_augroup('kickstart-highlight-yank', { clear = true }),
  callback = function()
    vim.highlight.on_yank()
  end,
})

-- Set colorscheme on file-type
vim.api.nvim_create_autocmd('BufEnter', {
  desc = 'Set coloscheme based on file type',
  group = vim.api.nvim_create_augroup('kgysu-colorscheme', { clear = true }),
  callback = function()
    if vim.bo.filetype == "zig" then
      vim.cmd.colorscheme("gruvbox")
    elseif vim.bo.filetype == "go" or vim.bo.filetype == "nix" then
      vim.cmd.colorscheme("gruvbox")
      -- vim.cmd.colorscheme("tokyonight-night")
    else
      vim.cmd.colorscheme("gruvbox")
    end
  end,
})


-- Configure Terminal
vim.api.nvim_create_autocmd('TermOpen', {
  desc = 'Configure terminal',
  group = vim.api.nvim_create_augroup('custom-term-open', { clear = true }),
  callback = function()
    vim.opt.number = false
    vim.opt.relativenumber = false
  end,
})



